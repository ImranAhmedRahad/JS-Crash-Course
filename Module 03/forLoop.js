//For Loop
for(let i = 1; i <= 100; i++){
    if(i%2 == 0){
        console.log("even number -", i);
    }else{
        console.log("odd number -", i);   
    }
}

//While Loop
let j =1;
while (j <=100){
    if(j%2==0){
        console.log("Even number -", j);    
    }else{
        console.log("Odd number -", j);    
    }
    j++
}

//Incremnet- Decrement 100-1
for(let i = 100; i>=1; i--){
    console.log(i);  
}